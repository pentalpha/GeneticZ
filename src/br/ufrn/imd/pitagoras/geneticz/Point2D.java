package br.ufrn.imd.pitagoras.geneticz;

/**
 * Ponto num plano cartesiano
 * @author pitagoras
 *
 */
public class Point2D {

	public int x, y;
	
	/**
	 * Construtor para a classe Point2D
	 * 
	 * @param x Coordenada X
	 * @param y Coordenada Y
	 */
	public Point2D(int x, int y) {
		this.x = x;
		this.y = y;
	}

}
